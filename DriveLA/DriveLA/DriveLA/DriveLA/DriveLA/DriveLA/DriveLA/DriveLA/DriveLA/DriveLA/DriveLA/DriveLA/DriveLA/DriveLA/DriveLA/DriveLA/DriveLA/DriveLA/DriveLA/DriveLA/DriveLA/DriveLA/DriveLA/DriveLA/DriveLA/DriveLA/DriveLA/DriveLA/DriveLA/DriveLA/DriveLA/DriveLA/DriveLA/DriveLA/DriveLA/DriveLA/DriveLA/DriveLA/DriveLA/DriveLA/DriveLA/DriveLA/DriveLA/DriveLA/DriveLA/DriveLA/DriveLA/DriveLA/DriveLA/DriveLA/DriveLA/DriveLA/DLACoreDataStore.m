#pragma mark - Application's Documents directory

// Returns the URL to the application's Documents directory.
- (NSURL *)applicationDocumentsDirectory
{
    return [[[NSFileManager defaultManager] URLsForDirectory:NSDocumentDirectory inDomains:NSUserDomainMask] lastObject];
}

- (Driver *)fetchDriver {
    
    NSEntityDescription *entityDescription = [NSEntityDescription entityForName:@"Driver" inManagedObjectContext:self.managedObjectContext];
    
    NSFetchRequest *request = [[NSFetchRequest alloc] init];
    [request setEntity:entityDescription];
    
    [request setReturnsObjectsAsFaults:NO];
    
    NSError *fetchError;
    NSArray  *array = [self.managedObjectContext executeFetchRequest:request error:&fetchError];
    
    
    if (array.count == 0) {
        return nil;
    }
    else if (array.count == 1) {
        return array[0];
    }
    else {
        NSLog(@"MULTIPLE DRIVERS FOUND");
        return array[0];
    }
}

- (void)updateDriverInfo:(NSDictionary *)info {
    
    Driver *existingDriver = [self fetchDriver];
    
    if (!existingDriver) {
        existingDriver = [NSEntityDescription insertNewObjectForEntityForName:@"Driver" inManagedObjectContext:self.managedObjectContext];
    }
    
    for (id key in info) {
        id value = [info objectForKey:key];
        [existingDriver setValue:value forKey:key];
    }
    
    [self saveContext];
}


@end
